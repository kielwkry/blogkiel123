            <h2>1. Aek Manik</h2>
<h5>Aek Manik
Image Credit: Lintaspublik.com
Sebuah wisata alam yang dikemas secara modern ini tidak pernah sepi pengunjung. Setiap hari Anda akan selalu melihat orang yang datang dengan wajah sumringah. Terang saja karena tempatnya sangat asri dan sejuk.

Cocok bagi Anda yang telah penat dengan pemandangan kota yang padat dan macet seperti Jakarta. Bila ada waktu luang silakan berkunjung ke Aek Manik untuk menikmati air dan kuliner yang ada di tempat ini.

Lokasi: Ambarisan, Sidamanik, Kab. Simalungun.</h5>

<h2>2. Vihara Patung Dewi Kwan Im </h2>

<h5>Vihara Patung Dewi Kwan Im
Image Credit: Enjoytourmedan.com
Kota Pematangsiantar memiliki penduduk yang beragam, salah satunya berasal dari Bangsa China. Kita sudah tahu bahwa wilayah Sumatera terdapat keturunan tionghua yang sudah hidup lama di tempat ini selama puluhan tahun. Selama itulah, penduduk pemeluk agama konghucu mendirikan kuil atau vihara ini. Patung Dewi Kwan Im yang ada di vihara ini sangat besar dengan warna putih. Anda dapat berfoto di dekat patung agar dapat membedakan ukuran yang sebenarnya.


Rekomendasi
Pulau Hinako, Spot Terbaik Menikmati Keindahan Biota Laut yang Beragam di Nias Barat
Batu Kursi Raja Siallagan, Destinasi Sejarah dengan Delapan Unit Rumah Batak di Samosir
Pantai Teluk Bengkuang, Spot Terbaik Menikmati Keindahan Pasir Putih di Nias Utara
Lokasi: Jl. Gn. Pusuk Buhit, Kebun Sayur, Kec. Siantar Timur, Kab. Pematang Siantar</h5>


<h2>3. Air Terjun Katasa </h2>
<h5>Air Terjun Katasa
Image Credit: Detik.com
Hal menarik dari air terjun ialah adanya dua anak sungai dari air terjun yang berbeda. Kedua air terjun tersebut menyatu dalam satu kolam sehingga membentuk sebuah situasi yang indah dan memukau. Hanya saja kolamnya agak dalam sehingga jika ingin berenang atau bermain air cukup berbahaya.

BACA JUGA:  10 Oleh-Oleh Khas Sibolga yang Paling Populer
Bagi Anda yang tidak dapat berenang sebaiknya jangan coba untuk menceburkan diri dalam kolam karena akan sangat berbahaya. Kemudian pada bagian atas air terjun ada sebuah jembatan putih untuk melintas para pejalan kaki yang melintas. Anda dapat memilih ingin menentukan spot mana untuk wisata.

Lokasi: Tonduhan, Hatonduhan, Kab. Simalungun.</h5>

<h2>4. Kebun Binatang Pematangsiantar </h2>
<h5>Kebun Binatang Pematangsiantar
Image Credit: Siantarzoo.com
Setidaknya ada 25 jenis satwa yang bisa Anda lihat di kebun binatang ini. Tentu saja yang menjadi primadona satwa di kebun binatang ini ialah buaya. Ada seekor buaya putih dan besar yang sudah hidup puluhan tahun. Buaya ini dirawat dengan baik karena kulitnya dapat dijadikan bahan utama untuk pembuatan dompet atau tas yang harganya cukup mahal.

Lokasi: Jl. Gunung Simanuk Manuk No. 2, Teladan, Kec. Siantar Barat, Kab. Pematang Siantar.</h5>

<h2>5. Museum Simalungun </h2>
<h5>Museum Simalungun
Image Credit: Jayakartanews.com
Sebuah bangunan tua dan masih sangat tradisional ini menjadi museum Kota Pematangsiantar yang sangat langka dan unik. Banyak peninggalan sejarah seperti senjata, alat makan, alat masak dan beragam pusaka lain yang menjadi salah satu saksi sejarah di daerah ini.

Wilayah ini meski kecil namun memiliki wisata yang lengkap dan bagus. Anda tidak hanya dimanjakan dengan pemandangan alam yang sejuk namun juga beragam situs sejarah yang akan membuat Anda tertarik mempelajari sejarah.


Lokasi: Jl. Jenderal Sudirman P, Proklamasi, Kec. Siantar Barat, Kab. Pematang Siantar.</h5>


